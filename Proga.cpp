#include <iostream>

int main() {
    setlocale(LC_ALL, "Russian");
    // Объявление переменной для хранения длины стороны квадрата
    double a;

    // Запрос у пользователя длины стороны квадрата
    std::cout << "Введите длину стороны квадрата: ";
    std::cin >> a;

    // Проверка на корректность ввода
    if (a < 0) {
        std::cout << "Ошибка: длина стороны не может быть отрицательной." << std::endl;
        return 1; // Завершение программы с кодом ошибки
    }

    // Вычисление площади квадрата
    double area = a * a;

    // Вывод результата
    std::cout << "Площадь квадрата со стороной " << a << " равна " << area << std::endl;

    return 0;
}
