#include <iostream>

int main() {
    setlocale(LC_ALL, "Russian");
    // Объявление переменных для хранения размеров параллелепипеда
    double a, b, c;

    // Запрос у пользователя размеров параллелепипеда
    std::cout << "Введите длину прямоугольного параллелепипеда: ";
    std::cin >> a;

    std::cout << "Введите ширину прямоугольного параллелепипеда: ";
    std::cin >> b;

    std::cout << "Введите высоту прямоугольного параллелепипеда: ";
    std::cin >> c;

    // Проверка на корректность ввода
    if (a < 0 || b < 0 || c < 0) {
        std::cout << "Ошибка: размеры не могут быть отрицательными." << std::endl;
        return 1; // Завершение программы с кодом ошибки
    }

    // Вычисление объёма параллелепипеда
    double volume = a * b * c;

    // Вывод результата
    std::cout << "Объём прямоугольного параллелепипеда с размерами "
        << a << " x " << b << " x " << c
        << " равен " << volume << std::endl;

    return 0;
}
